package org.dcm4che.test;import java.io.ByteArrayOutputStream;
import java.io.IOException;
import java.io.InputStream;
import java.net.MalformedURLException;
import java.net.URL;
import java.net.URLConnection;

/**
 * Created by player on 27-Nov-15.
 */
class MyClassLoader extends ClassLoader {

    private String customClassFullName;

    public MyClassLoader(ClassLoader parent, String customClassFullName) {
        super(parent);
        this.customClassFullName = customClassFullName;
    }

    @Override
    protected Class<?> loadClass(String name, boolean resolve) throws ClassNotFoundException {

        if(!customClassFullName.equals(name))
            return super.loadClass(name, false);

        try {
            String url = "file:C:/agfa/csp-sandbox/tests/integration-tests/dcm4che-test-integration/target/test-classes/org/dcm4che/test/CustomTester.class";
            URL myUrl = new URL(url);
            URLConnection connection = myUrl.openConnection();
            InputStream input = connection.getInputStream();
            ByteArrayOutputStream buffer = new ByteArrayOutputStream();
            int data = input.read();

            while (data != -1) {
                buffer.write(data);
                data = input.read();
            }

            input.close();

            byte[] classData = buffer.toByteArray();

            return defineClass(name, classData, 0, classData.length);

        } catch (MalformedURLException e) {
            e.printStackTrace();
        } catch (IOException e) {
            e.printStackTrace();
        }

        return null;
    }
}
